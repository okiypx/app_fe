	var pop = "chat.html";
	var mw = screen.availWidth; //�붾㈃ �볦씠
	var mh = screen.availHeight; //�붾㈃ �믪씠
	var w = 309; //�앹뾽李� 媛�濡�
	var h = 417; //�앹뾽李� �몃줈
